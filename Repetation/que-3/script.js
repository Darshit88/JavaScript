// Do_while Loop

let k = 99;
do {
    document.write('<br> K is : ' + k);
    k -= 11;
} while (k >= 11);



document.write('<br><br>');
document.write('<br><br>');
document.write('<br><br>');


// For Loop

for (let k = 99; k >= 11; k -= 11) {
    document.write('<br> K is : ' + k);
};

document.write('<br><br>');
document.write('<br><br>');
document.write('<br><br>');

// While Loop

let i = 99;

while (i >= 11) {
    document.write('<br> I is : ' + i);
    i -= 11;
}