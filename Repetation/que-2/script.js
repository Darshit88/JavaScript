// Do_while Loop

let j = 90;
do {
    document.write('<br> j is : ' + j);
    j -= 5;
} while (j >= 40);


document.write('<br><br>');
document.write('<br><br>');
document.write('<br><br>');


// For Loop

for (let j = 90; j >= 40; j -= 5) {
    document.write('<br> j is : ' + j);
};

document.write('<br><br>');
document.write('<br><br>');
document.write('<br><br>');

// While Loop

let i = 90;

while (i >= 40) {
    document.write('<br> i is : ' + i);
    i -= 5;
}