// Do_while Loop

let i = 1;
do {
    document.write('<br> I is : ' + i);
    i += 7;
} while (i <= 43);


document.write('<br><br>');
document.write('<br><br>');
document.write('<br><br>');

// For Loop

for (let i = 1; i <= 43; i += 7) {
    document.write('<br> I is : ' + i);
};

document.write('<br><br>');
document.write('<br><br>');
document.write('<br><br>');

// While Loop

let j = 1;
while (j <= 43) {
    document.write('<br> J is : ' + j);
    j += 7;
}




