// Do_while Loop

let d = 90;
do {
    document.write('<br> d is : ' + d);
    d += 100;
} while (d <= 790);



document.write('<br><br>');
document.write('<br><br>');
document.write('<br><br>');


// For Loop

for (let d = 90; d <= 790; d += 100) {
    document.write('<br> d is : ' + d);
};

document.write('<br><br>');
document.write('<br><br>');
document.write('<br><br>');

// While Loop

let l = 90;

while (l <= 790) {
    document.write('<br> L is : ' + l);
    l += 100;
}
